-- ============================================================
-- PRICE HISTORY for new drugs (6 months)
-- ============================================================
insert into public.price_history (drug_id, pharmacy_id, price, recorded_at)
select
  p.drug_id,
  p.pharmacy_id,
  round((p.price * (0.95 + (random() * 0.10)))::numeric, 2) as price,
  now() - (interval '1 month' * s.month_offset) as recorded_at
from public.prices p
cross join (select generate_series(1, 6) as month_offset) s
where p.drug_id like 'd%' or p.drug_id like 'e%'
order by p.drug_id, p.pharmacy_id, s.month_offset;
